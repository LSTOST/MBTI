import type { MbtiQuestion } from "@/lib/types";

function buildDimensionQuestions(
  dimension: MbtiQuestion["dimension"],
  leftPole: MbtiQuestion["leftPole"],
  rightPole: MbtiQuestion["rightPole"],
  rows: Array<[string, string, string]>,
  startIndex: number,
) {
  return rows.map(([prompt, leftLabel, rightLabel], offset) => ({
    id: `${dimension}-${offset + 1}`,
    index: startIndex + offset,
    dimension,
    prompt,
    leftPole,
    rightPole,
    leftLabel,
    rightLabel,
  }));
}

